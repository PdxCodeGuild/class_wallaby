def rot13(txt):
#------------------------------------------------------------------------#   
    result = []
    for letter in txt:
        value = ord(letter)
        if value >= ord('A') and value <= ord('Z'):
            value += 13
            if value > ord('Z'):
                value = (value - ord('Z')) + ord('A') - 1
 #------------------------------------------------------------------------#               
        elif value >= ord('a') and value <= ord('z'):
            value += 13
            if value > ord('z'):
                value = (value - ord('z')) + ord('a') - 1
        result.append(chr(value))
    return "".join(result)
#------------------------------------------------------------------------#
print("#-------------------------------------------------------------#")
while True:
   
    msg = input("please type a message to encrypt or decrypt: ").strip()
    
    if not msg:
        print("#-------------------------------------------------------------#")
        break
#------------------------------------------------------------------------#
    enc = rot13(msg)
    print("#-------------------------------------------------------------#")
    print("1:",enc)
    print("#-------------------------------------------------------------#")
    print("2:",rot13(enc))
    print("#-------------------------------------------------------------#")
